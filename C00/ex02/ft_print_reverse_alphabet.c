#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_reverse_alphabet(void)
{
	char i;

	i = 'z' + 1;
	while (--i >= 'a')
	{
		ft_putchar(i);
	}
}
