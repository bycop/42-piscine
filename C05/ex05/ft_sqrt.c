int		ft_sqrt(int nb)
{
	int i;

	i = -1;
	if (nb == 1)
		return (1);
	if (nb == 4)
		return (2);
	while (++i < nb / 2 && i < 46341)
		if (i * i == nb)
			return (i);
	return (0);
}
