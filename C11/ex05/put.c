#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	long int nbr;

	nbr = nb;
	if (nbr < 0)
	{
		ft_putchar('-');
		nbr = nbr * -1;
	}
	if (nbr >= 10)
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
	else
		ft_putchar(nbr % 10 + '0');
}

void	ft_put(int nb, int index)
{
	if (index == 2)
	{
		ft_putnbr(nb);
		ft_putchar('\n');
	}
	if (index == 0)
	{
		ft_putchar('0');
		ft_putchar('\n');
	}
}
